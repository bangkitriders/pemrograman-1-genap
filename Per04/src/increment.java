// Latihan Variabel
package Latihan01.sesi1.bin;

public class increment{
	
	public static void main(String[] bangkit){
		int a;
		a=120;
		System.out.println("Contoh postfix increment : ");
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		a=120;
		System.out.println("Contoh postfix increment : ");
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		a=120;
		System.out.println("Contoh postfix increment : ");
		System.out.println(a);
		System.out.println(a--);
		System.out.println(a);
		a=120;
		System.out.println("Contoh postfix increment : ");
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a);
	}	
}